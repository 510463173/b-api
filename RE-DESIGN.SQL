  CONSTRAINT FK_ARTICLE_ATTACHMENT_USER FOREIGN KEY (`USER`) references USER(`ID`)
)
ENGINE = InnoDB
AUTO_INCREMENT = 1
CHARSET = utf8
COMMENT = 'ARTICLE_ATTACHMENT';

DROP TABLE IF EXISTS `USER_COMMENT`;

